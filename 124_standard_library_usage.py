# I. MATH LIBRARY
# The following example demonstrates how to use the ceil function in the math library

# 1. Import the library
import math

x = 1.37
# 2. Call the ceil function in the math library using the dot operator
print("ceil(1.37) =", math.ceil(x))


# II. BUILT-IN FUNCTIONS
# The following example demonstrates how to use the len built-in function to determine the length of a string

# 1. Call the len function directly
string = "This is a string."
print("len('This is a string.') =", len(string))

